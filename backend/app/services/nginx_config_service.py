def generate_decoy_cert_shell_command(
    cert_path: str = DECOY_CERT_PATH,
    key_path: str = DECOY_KEY_PATH,
    cert_dir: str = DECOY_CERT_DIR,
) -> str:
    """Return the shell command to generate the decoy self-signed cert.

    The SSH service runs this once at deploy-time if the cert doesn't
    already exist. Uses openssl req with -nodes (unencrypted key) and a
    generic CN — clients only ever see this cert when they send an
    unknown SNI, so the common name is cosmetic.
    """
    # Using fixed string literals — no interpolation of user input, so we
    # only need the shell quoting we apply here in our own code.
    return (
        "mkdir -p " + _sh(cert_dir) + " && "
        "chmod 755 " + _sh(cert_dir) + " && "
        "if [ ! -f " + _sh(cert_path) + " ] || [ ! -f " + _sh(key_path) + " ]; then "
        "openssl req -x509 -nodes -newkey rsa:2048 "
        "-subj '/CN=cyberx-redirector-decoy' "
        "-days 3650 "
        "-keyout " + _sh(key_path) + " "
        "-out " + _sh(cert_path) + " "
        "2>/dev/null && "
        "chmod 644 " + _sh(cert_path) + " && "
        "chmod 600 " + _sh(key_path) + "; "
        "fi"
    )


def _sh(val: str) -> str:
    """Minimal shell-quote for static constants used in generated shell lines."""
    if not isinstance(val, str):
        raise ValueError("_sh expects a str")
    if any(c in val for c in "\"'$`\\\n;&|<>"):
        raise ValueError(f"Refusing to shell-quote unsafe value: {val!r}")
    return "'" + val + "'"


def generate_stream_config_preview(stream: "StreamConfig") -> str:
    """Alias for generate_stream_config — used by the preview endpoint."""
    return generate_stream_config(stream)
